package week_04;

public class RelationalOperators {

    public static void main(String[] args) {

        boolean value = 4 < 5; //less than
        System.out.println("value = " + value);
        System.out.println(4 < 5);

        boolean lessThanEqual = 4 <= 5;
        System.out.println("lessThanEqual = " + lessThanEqual);
        boolean graterThan = 5 > 4;

        System.out.println("graterThan = " + graterThan);


        boolean graterThanOrEqual=5>=4;


        System.out.println("graterThanOrEqual = " + graterThanOrEqual);

        boolean equal= 5==5;
        System.out.println("equal = " + equal);


        boolean notEqual= 4!=5;
        System.out.println("notEqual = " + notEqual);


    }
}